// Write-time near-duplicate detection (plan 013 WP4). `findNearDuplicates` is a pure
// cosine-over-weighted-tokens policy (title ×3, tags ×2, body ×1) enforced at the CLI
// edges (kb:add, kb:curate --supersede), never inside the store.
//
// These fixtures are FROZEN COPIES of real live entries (verbatim title/tags/body as of
// plan 013 WP4). They are copied in — the tests deliberately never read the live
// knowledge/ dir, so a future `--force`d or hand-authored similar pair can never redden
// CI. Cosine is pairwise-independent (unlike searchEntries' pool-relative IDF), so a
// pair's score here equals its score in the full 16-entry corpus.

import { test, expect } from "bun:test";
import { findNearDuplicates, DUP_THRESHOLD } from "../src/index";
import type { KnowledgeEntry } from "../src/index";

function mk(partial: Partial<KnowledgeEntry> & { id: string; title: string; body: string }): KnowledgeEntry {
  return { type: "lesson", tags: [], created: "2026-06-26", ...partial };
}

