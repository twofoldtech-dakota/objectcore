const FIXTURES: KnowledgeEntry[] = [
  INDEX_ARTIFACT,
  STORAGE_PORT,
  JUDGE_POOL,
  SUBAGENT_TOOLS,
  RELEASE_MERGE,
  TRIGGER_SURFACE,
];

/** A candidate view (title/tags/body) of an entry — what the CLIs pass. */
function asCandidate(e: KnowledgeEntry): { title: string; tags?: string[]; body: string } {
  return { title: e.title, tags: e.tags, body: e.body };
}

// --- identical → ~1.0 and trips ---------------------------------------------------

test("an identical candidate scores ~1.0 and trips the threshold", () => {
  const hits = findNearDuplicates(asCandidate(INDEX_ARTIFACT), FIXTURES);
  expect(hits.length).toBeGreaterThan(0);
  expect(hits[0]!.id).toBe("index-is-a-build-artifact");
  expect(hits[0]!.score).toBeCloseTo(1, 6);
  expect(hits[0]!.score).toBeGreaterThanOrEqual(DUP_THRESHOLD);
  expect(hits[0]!.title).toBe(INDEX_ARTIFACT.title);
});

// --- a paraphrase (same facts, reworded) still trips ------------------------------

test("a reworded copy of a real entry still trips the threshold", () => {
  const paraphrase = {
    title: "Never hand-edit the generated INDEX file",
    tags: ["knowledge-base", "gate"],
    body:
      "The knowledge base INDEX.md is a build artifact produced by kb:index, just like " +
      "marketplace.json. Editing it by hand fails the byte-match check in kb:check. " +
      "Regenerate it with kb:index instead of touching it directly; budget overflow is the " +
      "curate/prune signal.",
  };
  const hits = findNearDuplicates(paraphrase, FIXTURES);
  const match = hits.find((h) => h.id === "index-is-a-build-artifact");
  expect(match).toBeDefined();
  expect(match!.score).toBeGreaterThanOrEqual(DUP_THRESHOLD);
});

// --- distinct fixtures are all pairwise below the shipped threshold (the pin) ------

test("every distinct fixture pair scores below the shipped DUP_THRESHOLD", () => {
  let max = 0;
  let maxPair = "";
  for (let i = 0; i < FIXTURES.length; i++) {
    for (let j = 0; j < FIXTURES.length; j++) {
      if (i === j) continue;
      const a = FIXTURES[i]!;
      const b = FIXTURES[j]!;
      // threshold 0 → the raw pairwise score regardless of the cutoff.
      const [m] = findNearDuplicates(asCandidate(a), [b], { threshold: 0 });
      const score = m?.score ?? 0;
      expect(score).toBeLessThan(DUP_THRESHOLD);
      if (score > max) {
        max = score;
        maxPair = `${a.id} × ${b.id}`;
      }
    }
  }
  // The tightest legitimate pair is the two judge-vocabulary entries (~0.47) — the
  // calibrated worst case. It must remain safely under the shipped threshold.
  expect(max).toBeGreaterThan(0.4);
  expect(max).toBeLessThan(DUP_THRESHOLD);
  expect(maxPair).toContain("judge-pool-distractor");
});

// --- excludeIds excludes -----------------------------------------------------------

test("excludeIds drops the named entry from the comparison", () => {
  const cand = asCandidate(INDEX_ARTIFACT);
  const withIt = findNearDuplicates(cand, FIXTURES);
  expect(withIt.map((h) => h.id)).toContain("index-is-a-build-artifact");

  const without = findNearDuplicates(cand, FIXTURES, {
    excludeIds: ["index-is-a-build-artifact"],
  });
  expect(without.map((h) => h.id)).not.toContain("index-is-a-build-artifact");
  // Nothing else near-duplicates the index entry, so excluding it leaves no matches.
  expect(without).toEqual([]);
});

// --- zero-vector / empty candidate → no matches, no NaN ---------------------------

test("an empty or all-stopword candidate yields no matches and never NaN", () => {
  expect(findNearDuplicates({ title: "", body: "" }, FIXTURES)).toEqual([]);
  // Every token here is a stopword or a length-1 residue → empty vector.
  expect(findNearDuplicates({ title: "the a an is to of", body: "" }, FIXTURES)).toEqual([]);

  // A real candidate: every pairwise score is a finite number in [0,1], never NaN.
  const scored = findNearDuplicates(asCandidate(STORAGE_PORT), FIXTURES, { threshold: 0 });
  expect(scored.length).toBe(FIXTURES.length); // scores everything at threshold 0
  for (const m of scored) {
    expect(Number.isNaN(m.score)).toBe(false);
    expect(m.score).toBeGreaterThanOrEqual(0);
    expect(m.score).toBeLessThanOrEqual(1);
  }
});

// --- opts.threshold override respected ---------------------------------------------

test("opts.threshold overrides the default cutoff in both directions", () => {
  // The judge-vocabulary pair (~0.47) is below the default 0.57 → no match by default…
  const byDefault = findNearDuplicates(asCandidate(JUDGE_POOL), [TRIGGER_SURFACE]);
  expect(byDefault).toEqual([]);
  // …but a lower threshold surfaces it.
  const lowered = findNearDuplicates(asCandidate(JUDGE_POOL), [TRIGGER_SURFACE], {
    threshold: 0.4,
  });
  expect(lowered.map((h) => h.id)).toEqual([
    "a-trigger-surface-is-gated-whether-it-s-a-skill-or-an-agent-same-coverage-readiness-judge-path",
  ]);
  // A threshold above 1 suppresses even an identical candidate.
  const impossible = findNearDuplicates(asCandidate(INDEX_ARTIFACT), FIXTURES, {
    threshold: 1.01,
  });
  expect(impossible).toEqual([]);
});

// --- filter-agnostic: archived entries in the list are still compared -------------

test("findNearDuplicates compares whatever entries it is handed (status-agnostic)", () => {
  // The FILTERING (active-only) is the caller's job — this function does not look at
  // status. An archived copy passed in the list is still scored + returned.
  const archived: KnowledgeEntry = {
    ...INDEX_ARTIFACT,
    id: "index-is-a-build-artifact-archived",
    status: "superseded",
    supersededBy: "some-replacement",
  };
  const hits = findNearDuplicates(asCandidate(INDEX_ARTIFACT), [archived]);
  expect(hits.map((h) => h.id)).toEqual(["index-is-a-build-artifact-archived"]);
  expect(hits[0]!.score).toBeCloseTo(1, 6);
});

// --- deterministic sort: score DESC then id ASC ------------------------------------

test("matches sort by score desc then id asc (deterministic tie-break)", () => {
  // Two entries with identical content but different ids → identical scores → id-asc.
  const zzz: KnowledgeEntry = { ...INDEX_ARTIFACT, id: "zzz-copy" };
  const aaa: KnowledgeEntry = { ...INDEX_ARTIFACT, id: "aaa-copy" };
  const hits = findNearDuplicates(asCandidate(INDEX_ARTIFACT), [zzz, aaa]);
  expect(hits.map((h) => h.id)).toEqual(["aaa-copy", "zzz-copy"]);
  expect(hits[0]!.score).toBe(hits[1]!.score);
});
